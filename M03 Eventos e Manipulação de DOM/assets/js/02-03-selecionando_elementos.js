// Todos os códigos são usados no console do navegador.

// "document.getElementById("?")" usado para buscar pelo "id".

document.getElementById("teste")

// ATENÇÂO! Sempre que "getElements" estiver no plural ele retornará um "array".

// "document.getElementsByTagName("?")" usado para buscar pela "TagName".

document.getElementsByTagName("h1")

// "document.getElementsByClassName("?")" usado para buscar pelo "ClassName".

document.getElementsByClassName("botao")

// Para selecionar usando: "document.querySelector("") and document.querySelectorAll("")" funcionam como se estivessemos no CSS. 

// "document.querySelector("")" selecionando pelo "id."

document.querySelector("#teste")

// "document.querySelectorAll("")" sempre retornará uma "array".

document.querySelectorAll("")
