let profession = "Professor"

console.log("Profissão: " + profession)

switch (profession) {

    case 'bombeiro' :
        console.log('Sua camisa será VERMELHA');
        break;

    case 'policial' :
        console.log('Sua camisa será AZUL');
        break;

    case 'fiscal' :
        console.log('Sua camisa será VERDE');
        break;

    default:
        console.log('Sua camisa será PRETA');
        break

}